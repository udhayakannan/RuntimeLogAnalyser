    private void refreshTextArea(String inputSearch, BufferedReader brSearch) {
        String line;
        //   inputSearch =date;
      //  System.out.println(inputSearch);
        StringBuilder bufferSearch = new StringBuilder();
        try {
            while ((line = brSearch.readLine()) != null) {
              //  System.out.println(line);
                if (line.equalsIgnoreCase(inputSearch)
                        || line.startsWith(inputSearch)
                        || line.endsWith(inputSearch)
                        || line.contains(inputSearch)) {
                    bufferSearch.append(line).append('\n');
                }
            }
            jTextArea1.setText(bufferSearch.toString());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadLogsFiles() {
        File dir = new File(".");
        FilenameFilter filter = new FilenameFilter() {

            public boolean accept(File dir, String name) {

                return (name.endsWith(".out") || name.endsWith(".txt") || name.endsWith(".log"));
            }
        };
        String[] children = dir.list(filter);
        if (children == null) {
            System.out.println("Either dir does not exist or is not a directory");
        } else {
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(children));
            for (int i = 0; i < children.length; i++) {
                String filename = children[i];
                new javax.swing.JLabel(filename);
              //  System.out.println(filename);
            }
        }
    }

