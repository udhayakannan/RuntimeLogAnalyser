    private void fileReader(String filename, String inputSearch, String inputSearch1, String inputSearch2, boolean isSearch, boolean isSearch1) {
        try {
            br = new BufferedReader(new FileReader(filename));
            String sCurrentLine;
            StringBuilder sb = new StringBuilder();
            jTextArea1.setText("");

            if (isSearch) {
                refreshTextArea(inputSearch, br);
            } else if (isSearch1) {
                daterange(inputSearch1, inputSearch2, br);
            } else {
                while ((sCurrentLine = br.readLine()) != null) {
                //    System.out.println(sCurrentLine);
                    sb.append(sCurrentLine).append('\n');
               //     System.out.println(inputSearch);
                }
                jTextArea1.setText(sb.toString());
            }

        } catch (IOException e) {
            e.printStackTrace();
        }
    }
